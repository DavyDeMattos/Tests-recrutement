<h2>
  Un client concessionnaire vous demande de lui créer une petite application web pour gérer ses voitures :
</h2>
<ul>
  <li>
    Un tableau de bord affichant la liste des voitures rentrées
    <ul>
      <li>Avec comme colonnes : marque, modèle, plaque immatriculation, carburant, prix, type vente (occasion ou neuve), réservé (oui ou non)</li>
    </ul>
  </li>
  <li>
    Il sera possible à partir de ce tableau de bord de :
    <ul>
      <li>Ajouter une voiture</li>
      <li>Supprimer une voiture</li>
      <li>Modifier une voiture</li>
    </ul>
  </li>
  <li>Enfin, à chaque modification il ne souhaite qu’un rafraichissement du tableau de bord et non de toute la page.</li>
</ul>
<p><strong>Stack imposée</strong> : PHP 8.1, JS ES5, CSS3, HTML5</p>
<p><strong>Quelques consignes supplémentaires :</strong></p>
<ul>
  <li>Démontrer que vous maîtrisez la Programmation orientée objet en PHP en développant
from scratch (pas d’utilisation de framework)</li>
  <li>Gérer bien les différents cas d’erreur</li>
  <li>Utiliser un design pattern de type MVC (Model View Controller), des services peuventaussi être utilisés</li>
  <li>Un autoload avec composer (utiliser la dernière version)</li>
  <li>Fournir un dump de votre base pour les tests</li>
</ul>
<ul>Stack optionnelle :
  <li>Utilisation de docker</li>
</ul>

<em>Attention le beau (côté affichage web) n’est que secondaire, le client veut du fonctionnel !</em>