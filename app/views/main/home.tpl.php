<h2>
  Un client concessionnaire vous demande de lui créer une petite application web pour gérer ses
  voitures :
</h2>
<ul>
  <li>
    Un tableau de bord affichant la liste des voitures rentrées
    <ul>
      <li>Avec comme colonnes : marque, modèle, plaque immatriculation, carburant, prix,
type vente (occasion ou neuve), réservé (oui ou non)</li>
    </ul>
  </li>
  <li>
    Il sera possible à partir de ce tableau de bord de :
    <ul>
      <li>Ajouter une voiture</li>
      <li>Supprimer une voiture</li>
      <li>Modifier une voiture</li>
    </ul>
  </li>
  <li>Enfin, à chaque modification</li>
</ul>